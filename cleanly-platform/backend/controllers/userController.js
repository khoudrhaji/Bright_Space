const User = require('../models/User');

// @desc    Get all providers
// @route   GET /api/users/providers
// @access  Public
const getProviders = async (req, res) => {
    try {
        const providers = await User.find({ role: 'provider' });
        res.json(providers);
    } catch (error) {
        res.status(500).json({ message: error.message });
    }
};

